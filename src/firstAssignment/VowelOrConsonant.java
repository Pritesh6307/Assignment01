package firstAssignment;

public class VowelOrConsonant {

	public static void main(String[] args) {
		char ch = 'a';

		if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' 
				|| ch == 'u' || ch == 'A' || ch == '0' || ch == 'U') {

			System.out.println("Given Character is Vowel");

		} else {

			System.out.println("Given Character is Consonant");

		}

	}

}