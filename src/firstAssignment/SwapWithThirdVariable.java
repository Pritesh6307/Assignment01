package firstAssignment;

public class SwapWithThirdVariable {

	public static void main(String[] args) {
		int a = 10;

		int b = 20;

		int c;

		System.out.println("Before Swapping");

		System.out.println(a);

		System.out.println(b);

		c = a;

		a = b;

		b = c;

		System.out.println("After Swapping");

		System.out.println(a);

		System.out.println(b);

	}

}